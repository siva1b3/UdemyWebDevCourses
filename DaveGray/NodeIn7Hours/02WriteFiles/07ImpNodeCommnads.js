// Here are some important commands 
// from the Node.js fs module that you may find useful:

// Reading Files:
// fs.readFileSync(path[, options]): Reads the contents of a file synchronously.
// fs.readFile(path[, options], callback): Reads the contents of a file asynchronously.

// Writing Files:
// fs.writeFileSync(file, data[, options]): Writes data to a file synchronously.
// fs.writeFile(file, data[, options], callback): Writes data to a file asynchronously.

// Appending to Files:
// fs.appendFileSync(file, data[, options]): Appends data to a file synchronously.
// fs.appendFile(file, data[, options], callback): Appends data to a file asynchronously.

// Renaming or Moving Files:
// fs.renameSync(oldPath, newPath): Renames or moves a file synchronously.
// fs.rename(oldPath, newPath, callback): Renames or moves a file asynchronously.

// Checking File Existence:
// fs.existsSync(path): Synchronously checks if a file exists.
// fs.exists(path, callback): Asynchronously checks if a file exists.

// Deleting Files:
// fs.unlinkSync(path): Deletes a file synchronously.
// fs.unlink(path, callback): Deletes a file asynchronously.

// Checking File Information:
// fs.statSync(path): Returns information about a file synchronously.
// fs.stat(path, callback): Returns information about a file asynchronously.
